    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        // dd($request);
        $payment=new Bill;
        $payment->amount=$request->amount;
        $payment->kase_id=$request->case;
        $payment->date=$request->date;
        $payment->save();
        $case=Kase::find($request->case);
        $total_received=$case->received+$request->amount;
        $remaining=$case->total-$total_received;
        $case->received=$total_received;
        $case->remaining=$remaining;
        $case->save();

        
        return redirect()->route('kase.show',['id'=>$case->id]);
    }

